    //FunÃ§Ã£o que carrega todos os providers existentes no sistema
    public static void loadProviders() {
        Provider providers[] = Security.getProviders();
        //todos os fornecedores do seguranÃ§a
        for (Provider provider : providers) {
            StringBuilder txt = new StringBuilder();
            List<String> lst = new ArrayList<>();
            //serviÃ§os fornecidos
            Set<Provider.Service> services = provider.getServices();
            for (Provider.Service service : services) {
                lst.add(String.format("%-20s %s\n", service.getType(), service.getAlgorithm()));
            }
            Collections.sort(lst);
            for (String service : lst) {
                txt.append(service);
            }
        }
    }
